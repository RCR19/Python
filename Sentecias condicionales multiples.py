print("¡¡convertidor de nuemeros a letras!!")

numero = int(input("¿cual es el numero que deseas convertir?: "))

if numero == 1:
    print("el numero es 'uno'")
elif numero == 2:
    print("el numero es 'dos'")
elif numero == 3:
    print("el numero es 'tres'")
elif numero == 4:
    print("el numero es 'cuatro'")
elif numero == 5:
    print("el numero es 'cinco'")

else:
    print("Lo sentimos este programa solo puede convertir hasta el numero 5")

print("Fin.")
